    nagyra értékeljük az önkéntes munkát.
    </p>
    <p>
    Hiszünk az együttműködés, a közös munka erejében, keressük a 
    kapcsolatot a hasonlóan gondolkodó állampolgárokkal és szervezetekkel, 
    fejlesztjük az ilyen hálózatokat, közvetítjük megszerzett tudásunkat és 
    segítünk mások tapasztalatainak közreadásában.
