    hogy közben a jövő generációk életlehetőségei ne csökkenjenek. 
    Miközben cselekvően fellépünk a környezetszennyező tevékenységek ellen, 
    fontosnak tartjuk a szemléletformálást és oktatást is.
    </p>
    <p>
