    <h4>HELYZETELEMZÉS</h4>
    <p>
    Az Életfa Környezetvédő Szövetség az elmúlt időszakban kialakította 
    profilját és kikristályosodtak önálló munkaformái, kiépítette hálózati 
    jellegű együttműködési rendszerét és jelentőset lépett előre a korszerű 
    kommunikációs módszerek alkalmazása területén.
    </p>
    <p>
    Fejlődését mégis veszély fenyegeti, mert a környezet- és természetvédelmi 
    környezet negatív tendenciáival, a civil információs, szolgáltatói 
    szerepkör felvállalásával és az állami támogatási rendszer változásával 
    párhuzamosan erősödtek az önállóságát veszélyeztető önkormányzati és 
    állami, direkt módon megnyilvánuló elvárások és finanszírozási módszerek. 
    A hátrányos következmények kivédését az alaptevékenység keretében 
    folytatott szolgáltatási tevékenység és a civil együttműködési rendszer 
    erősítésével célszerű kivédeni, kiemelt figyelmet fordítva a szerződéses 
    partneri kapcsolatokra. Az elmúlt időszakban a Szövetség munkájánál bevált 
    a „nyilvánosság és állásfoglalások” érdekérvényesítési munkaforma. 
    A szakértői háttérmunka javítására, a nyilvánosság erősítésére, és a 
    lakossági kommunikáció jobb megszervezésére kell kiemelt figyelmet 
    fordítani.
    </p>
    <p>
    Az Internet várhatóan a közeljövőben még nagyobb szerepet fog játszani a 
    lakossággal és tagsággal folytatott interaktív véleménycserében. 
    Ezen a téren felkészültek vagyunk, az Egri Civil Kapu portálrendszer és 
    webszerver segítségével minden projektünket internetes eszközökkel 
    támogathatjuk.
    </p>
    <p>
    A „Zöld Telefon Szolgálat” jelentős szerepet tölt be a lakosság által 
    bejelentett problémák megoldásánál. A nemzetközi, országos, és 
    helyi szervezetekkel kialakított kapcsolat jól elősegítette a különböző 
    ügyek megoldását.
    </p>
    <p>
    Több nemzetközi és hazai szervezettel, hálózattal élő kapcsolatot tartunk 
    és célorientált együttműködést valósítunk meg (pl.: KÖT, HUMUSZ, 
    Energia Klub, MOTT Alapítvány, Inforce, MTVSZ, NIOK, CISZOK stb.). 
    Több program megvalósításánál működtünk és működünk együtt a proHáló 
    hálózattal, amely alapításában részt vettünk és tevékenységébe aktívan 
    bekapcsolódunk.
    </p>
    <p>
    Rendszeresen részt veszünk a környezet- és természetvédő szervezetek 
    országos találkozóin. Egyre nagyobb szerepet kap a határmenti civil 
    hálózatokkal, szervezetekkel kialakuló projekt együttműködés: Erdélyi 
    Magyar Civil Szervezetekért Alapítvány (Románia - Kolozsvár), Fundament 
    Polgári Szervezet (Szlovákia - Rimaszombat), Koháry Polgári Társulás 
    (Szlovákia - Fülek).
    </p>
