<h1>Küldetésünk</h1>
<h4>"Egy fenntartható és emberibb társadalomért"</h4>
<p>
    Szervezetünk két nagy területen dolgozik, ezek egyrészt a 
    környezetvédelem, másrészt a nonprofit szervezetek, 
    vagyis tágabban a civil társadalom fejlesztése.
</p>
<h4>Környezetvédelem</h4>
<ul>
    <li>
    Célunk, hogy átfogó keretet biztosítsunk mindennemű környezetvédelmi 
    törekvésnek, felismerjük-megismertessük a környezetvédelmi problémákat, 
    és szakszerűen, 
    következetesen cselekedjünk azok elhárítása érdekében.
    </li>
    <li>
    Támogatjuk a környezetvédelmi nevelést és szemléletformálást, 
    az épített és természeti 
    környezettel kapcsolatos pozitív értékrendek kialakítását.
    </li>
    <li>
    Alapelvünk hogy minden állampolgárnak joga van az egészséges, 
    biztonságos és esztétikus környezethez. Ennek az alapvető emberi 
    jognak a védelmében együttműködünk minden olyan szervezettel, 
    amely hasonló célokat tűzött maga elé.
    </li>
    <li>
    Részt veszünk a környezetvédelmi törvény szabályai szerint a 
    környezetvédelmi engedélyezési eljárásokban, közreműködünk a 
    tevékenységi területünket érintő területfejlesztési, területrendezési 
    tervek és környezetvédelmi programok kidolgozásában, véleményezzük a 
    környezettel kapcsolatos állami és önkormányzati jogszabályok tervezeteit.
    </li>
    </ul>
    <h4>Civil szektorfejlesztés</h4>
    <ul>
    <li>
    Elősegítjük az állampolgárok, azok szervezetei és önkormányzatok 
    közötti együttműködést, kommunikációt, elősegítsük a fenntartható, 
    erős helyi közösségeken alapuló, demokratikus társadalom megvalósulását. 
    Munkánkkal növeljük a 
    társadalmi tőkét, fejlesztjük a közösségi részvételt.
    </li>
    <li>
    Célunk egy dinamikus, a kormányzati, önkormányzati és 
    profitorientált szektorral harmonikusan együttműködő, 
    sajátos szerepét vállaló nonprofit szektor kialakítása és fejlesztése.
    </li>
    <li>
    Támogatjuk a civil szektor szakmai és területi hálózatainak létrejöttét, 
    támogatjuk működésüket erőforrásainkkal, képzésekkel, tanácsadással, 
    szakmai munkánkkal, online és nyomtatott publikációkkal. 
    Együttműködünk a céljainkkal egyetértő hálózatokkal, szervezetekkel és 
    más szereplőkkel.
    </li>
    <li>
    Maximálisan támogatjuk, tevékenyen fejlesztjük a szabad 
    információáramlást és tudásmegosztást, 
    a társadalom minden szereplője és szektora felé.
    </li>
</ul>
