<h1>ETIKAI KÓDEX - ÉLETFA</h1>

<p>
    Az Életfa Környezetvédő Szövetség etikai kódexének célja, 
    hogy kinyilvánítsuk azokat az alapvető elveket, melyek mentén 
    az Egyesület tevékenységét végzi, s amelyeket tagjaink is szem 
    előtt tartanak mindennapi életük során, 
    felállítva közösségünk szellemiségét és értékrendjét.
</p>
<ol>
    <li>Egyesületünk legfontosabb célja a környezet védelme, 
        védett természeti és társadalmi értékeink megőrzése. 
        Ezért támogatunk minden ilyen törekvést és programot, 
        egyesületünk tagjai pedig magánemberként is óvják környezetüket, 
        fenntartható életmódra törekszenek és erre szólítanak fel másokat is.
    </li>
    <li>
    Programjaink és működésünk során, energiafelhasználásunk 
    terén előnyben részesítjük a környezetbarát, megújuló forrásból 
    származó energiákat. Hulladékot mindig a megfelelő tárolóban 
    helyezünk el, lehetőség szerint szelektíven gyűjtjük és ügyelünk 
    arra, hogy a lehető legkevesebb keletkezzen belőle. Kiemelt figyelmet 
    fordítunk a veszélyes hulladékok elkülönítésére 
    és az újrahasznosítást biztosító gyűjtőhelyekre való eljuttatására.
    </li>
    <li>
    Fontosnak tartjuk a társadalom demokratikus működését, 
    az emberi jogok szabad gyakorlását, a törvény előtti egyenlőséget, 
    a nyíltságot, átláthatóságot, a szabad információáramlást, ezekért 
    a célokért, a társadalmi tőke fejlesztéséért tevékenyen dolgozunk és 
    együttműködünk 
    szakmai és területi hálózatokkal, más hasonló szervezetekkel.
    </li>
    <li>
    Az Életfa Környezetvédő Szövetség nem végez politikai tevékenységet,
    nem indul választásokon, nem részesít előnyben politikai pártokat és 
    mozgalmakat más pártokkal vagy mozgalmakkal szemben. Munkája során csak 
    szakmai, környezetvédelmi és nonprofit érdekvédelmi szempontokat vesz 
    figyelembe. Éppen ezért visszautasít mindenféle politikai részvételt, 
    befolyásolási szándékot, politikai véleményt és támogatást, mely 
    tevékenységét érintené.
    Ugyanakkor fenntartjuk magunknak a jogot, hogy a 
    mindenkori hatalmi és gazdasági szereplőkről véleményt nyilvánítsunk, 
    rájuk nyomást gyakoroljunk, céljaink érdekében minden demokratikus 
    eszközt latba vessünk, akár ezen szereplők szándékaival szemben is. 
    </li>
    <li>
    Fontosnak tartjuk a saját működésünk átláthatóságának biztosítását. 
    Valljuk, hogy egy civil szervezetek legfontosabb értéke a bizalom, a 
    társadalmi beágyazottság, a helyi elismertség, amely a közösségben 
    betöltött szerepét és eredményességét nagyban meghatározza.
    Mindent elkövetünk, hogy ne csak a saját hitelességünket, hanem a 
    nonprofit szektor hitelességét is erősítsük.
    </li>
    <li>
        <p>
            Adminisztratív működésünkben és kommunikációnkban egyaránt világos, 
            nem félrevezető elveket követünk.
        </p>
        <p>
            Az általunk közzétett adatok mindig megfelelnek a valóságnak, 
            elkerüljük a félretájékoztatást,
            elítéljük az álhírek terjesztését.
        </p>
    </li>
    <li>
    Törekszünk a bizalom megőrzésére, rövid határidőn belül 
    válaszolunk és intézkedünk, 
    amennyiben kérdés vagy panasz érkezik hozzánk.
    </li>
    <li>
    Törvényesen működünk. Betartunk minden, a működésünket 
    érintő törvényt és egyéb jogszabályt.
    Amennyiben szabálytalanságot észlelünk, azonnal lépéseket teszünk 
    azok megszüntetésére, kijavítására. A támogatások 
    gyűjtését törvényesen és tisztességesen, létesítő okiratunknak megfelelően 
    végezzük. Tervszerűen és követhetően működünk és gazdálkodunk. 
    Működési dokumentumainkba (az adatvédelmi és személyiségi jogok 
    betartásával) minden érintettnek betekintési lehetőséget biztosítunk.
    </li>
    <li>
    Szervezetünk rendszeresen, a törvényesen előírt minimális mértéket 
    meghaladóan hoz nyilvánosságra adatokat és információkat tevékenységéről 
    és gazdálkodásáról.
    Legfőbb szervünk (küldöttgyűlés vezetőség stb.) üléseinek időpontját, 
    meghívóját, napirendjét nyilvánosságra hozzuk, biztosítjuk a részvétel 
    lehetőségét (kivéve, ha azt adatvédelmi illetve személyiségi jogok miatt 
    korlátozni szükséges).
    Honlapunkon, kezdőlapról elérhetően elhelyezzük kötelező dokumentumainkat, 
    vagyis szervezetünk bemutatását, közhasznú beszámolóinkat, kapcsolati és 
    alapadatainkat, vezetőségünk adatait, szervezeti stratégiánkat és jelen 
    etikai kódexünket.
    </li>
    <li>
    Barátsággal és együttműködéssel fordulunk minden hasonló elvet valló
    polgár és szervezet felé, nyitottak vagyunk a párbeszédre, megosztjuk 
    tapasztalatainkat és tudásunkat.
    </li>
    <p>
    Minden tagunktól elvárjuk, hogy elfogadja e kódexben megfogalmazott 
    iránymutatásokat, sajátjának érezze közösségünk szellemiségét és 
    figyelemmel legyen egyesületünk jó hírnevének, függetlenségének 
    megőrzésére, törekedjen ezek erősítésére.
    </p>
</ol>
